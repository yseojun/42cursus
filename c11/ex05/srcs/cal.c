int	cal_add(int a, int b)
{
	return (a + b);
}

int	cal_sub(int a, int b)
{
	return (a - b);
}

int	cal_dup(int a, int b)
{
	return (a * b);
}

int	cal_div(int a, int b)
{
	return (a / b);
}

int	cal_mod(int a, int b)
{
	return (a % b);
}
