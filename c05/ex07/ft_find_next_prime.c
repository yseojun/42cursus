int	ft_find_next_prime(int nb)
{
	int	i;

	if (nb <= 2)
		return (2);
	i = 3;
	if (nb % 2 == 0)
		nb++;
	while (i <= nb / i)
	{
		if (nb % i == 0)
		{
			i = 1;
			nb += 2;
		}
		i += 2;
	}
	return (nb);
}
