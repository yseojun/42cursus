int	ft_atoi(char *str)
{
	int	d;
	int	ans;

	d = 1;
	ans = 0;
	while (*str == ' ' || (9 <= *str && *str <= 13))
		str++;
	while (*str == '-' || *str == '+')
	{
		if (*str == '-')
			d = -d;
		str++;
	}
	while ('0' <= *str && *str <= '9')
	{
		ans = ans * 10 + *str - '0';
		str++;
	}
	return (ans * d);
}
