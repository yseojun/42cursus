int	ft_strcmp(char *s1, char *s2)
{
	int	n;

	n = 0;
	while (s1[n] || s2[n])
	{
		if (s1[n] != s2[n])
			break ;
		n++;
	}
	return ((unsigned char)s1[n] - (unsigned char)s2[n]);
}
